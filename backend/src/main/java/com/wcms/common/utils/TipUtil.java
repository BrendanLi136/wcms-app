package com.wcms.common.utils;

public class TipUtil {
    public static String getTip(String age, String sex, String history) {
       String tip = "根据上传的伤口图片和已有患者信息【年龄、性别、病史（可能为空）】 返回伤口类型，分析结果，伤口类型按小类分，没有小类就写大类\n" +
               "年龄：%s\n" +
               "性别：%s\n" +
               "病史：%s\n" +
               "严格按以下示例返回\n" +
               "===示例开始===\n" +
               "{\n" +
               "  \"wound_type\": \"擦伤\""+ ",\n"  +
               "  \"analysis_result\" : \"表皮层受损，创面有渗血或组织液渗出，如摔倒后的皮肤损伤。\"\n" +
               "}\n" +
               "===示例结束===\n" +
               "\n" +
               "分类范围严格按以下固定返回（伤口类型可配置）\n" +
               "1. 按皮肤完整性划分\n" +
               "• 开放性损伤：指皮肤或黏膜的完整性被破坏，内部组织与外界相通。这类伤口有肉眼可见的破口，感染风险较高，需要及时清洁和处理。主要包括：\n" +
               "\n" +
               "  ◦ 擦伤：表皮层受损，创面有渗血或组织液渗出，如摔倒后的皮肤损伤。\n" +
               "\n" +
               "  ◦ 切割伤/锐器伤：由刀、玻璃等锐器造成，伤口边缘通常比较整齐，可能伤及深层血管。\n" +
               "\n" +
               "  ◦ 刺伤：由钉子、针等细长锐器导致，伤口小而深，因容易发生厌氧菌感染（如破伤风）而需要特别警惕。\n" +
               "\n" +
               "  ◦ 撕裂伤：皮肤因暴力牵拉而裂开，创缘不规则，常见于机器绞伤或动物咬伤。\n" +
               "\n" +
               "• 闭合性损伤：皮肤黏膜保持完整，但内部组织或器官可能已受损。这类损伤有时容易被忽视，可能伴随严重并发症。主要包括：\n" +
               "\n" +
               "  ◦ 挫伤：俗称“淤青”，是皮下组织受外力撞击出血所致，但表皮无破口。\n" +
               "\n" +
               "  ◦ 扭伤：关节周围的韧带、肌腱等软组织过度牵拉或撕裂，如脚踝扭伤。\n" +
               "\n" +
               "  ◦ 挤压伤：肌肉、血管或神经等组织受重物压迫导致损伤。\n" +
               "\n" +
               "  ◦ 闭合性骨折：骨骼断裂但皮肤未破。\n" +
               "\n" +
               "2. 按污染和感染程度划分\n" +
               "\n" +
               "这个维度直接影响医生的治疗策略，尤其是消毒和用药选择。\n" +
               "\n" +
               "• 清洁伤口：例如未经污染的手术切口。这类伤口无菌，感染风险低，愈合预期较好。\n" +
               "\n" +
               "• 污染伤口：指已接触污染物（如泥土、异物），但尚未出现明显感染的伤口。需要及时彻底清创，并可能需要预防性使用抗生素。\n" +
               "\n" +
               "• 感染伤口：指伤口已出现细菌感染，表现为红肿、化脓、疼痛加剧等。处理关键在于彻底清创、充分引流并使用有效抗生素。\n" +
               "\n" +
               "3. 按愈合时间和特性划分\n" +
               "\n" +
               "这个分类有助于判断伤口的愈合趋势和所需干预措施。\n" +
               "\n" +
               "• 急性伤口：通常由突发的外伤或手术引起，愈合过程符合典型的生理阶段，预期能在较短时间内（如2至4周）规律愈合。处理得当非常重要，否则可能转为慢性伤口。\n" +
               "\n" +
               "• 慢性伤口：指无法通过正常有序的修复过程达到愈合，治疗时间超过1个月，甚至无愈合倾向的创面。其形成往往与内在疾病密切相关，常见类型包括：\n" +
               "\n" +
               "  ◦ 压疮（压力性溃疡）：由于身体局部组织长期受压引起，常见于长期卧床或行动不便需要久坐的人群。\n" +
               "\n" +
               "  ◦ 糖尿病足溃疡：糖尿病患者因血管和神经病变，足部在轻微损伤后也极易形成难以愈合的溃疡。\n" +
               "\n" +
               "  ◦ 下肢血管性溃疡：主要由下肢静脉曲张或动脉粥样硬化等血管疾病导致。";


        return String.format(tip, age, sex, history);
    }

    public static void main(String[] args) {
        String tip = TipUtil.getTip("25", "男", "无");
        System.out.println(tip);
    }
}
